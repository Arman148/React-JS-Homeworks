function multiplyBy () {
    var x = document.getElementById('firstNumber').value;
    var y = document.getElementById('secondNumber').value;
    document.getElementById('result').innerHTML = x * y;
}

function divideBy () {
    var x = document.getElementById('firstNumber').value;
    var y = document.getElementById('secondNumber').value;
    document.getElementById('result').innerHTML = x / y;
}
